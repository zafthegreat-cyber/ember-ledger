export function money(value) {
  return `$${Number(value || 0).toFixed(2)}`;
}

export function getCatalogProduct(catalogId) {
  return CATALOG_PRODUCTS.find((product) => product.id === catalogId);
}

export function createInventoryItemFromCatalog({
  catalogId,
  quantity = 1,
  paidPriceEach,
  sellingPriceEach,
  purchaseSource = "",
  location = "Home",
  status = "In Stock",
}) {
  const product = getCatalogProduct(catalogId);

  if (!product) {
    throw new Error("Catalog product not found.");
  }

  return {
    id: `inv-${catalogId}-${Date.now()}`,
    catalogId,
    product,
    quantity: Number(quantity || 1),
    paidPriceEach:
      paidPriceEach !== undefined && paidPriceEach !== ""
        ? Number(paidPriceEach)
        : Number(product.msrpPrice || 0),
    sellingPriceEach:
      sellingPriceEach !== undefined && sellingPriceEach !== ""
        ? Number(sellingPriceEach)
        : Number(product.marketPrice || 0),
    purchaseSource,
    location,
    status,
  };
}